package com.LoopStatement;

public class Nested_Loop_Example1 {

	public static void main(String[] args) {
		
		// outer loop
		for(int index1=1;index1<=3;index1++)
		{
		// inner loop
		for(int index2=0;index1>=index2;index2++)
		{
		System.out.println(" Testing ");
		}
		}

		System.out.println(" **********  2  ********** ");
		System.out.println();
   
		for(int i=1;i<=3;i++)  //outer loop
		{
		for(int j=0;j<i;j++)   //inner loop
		{
		System.out.println(j);      
		}
		}

		System.out.println(" **********  3  ********** ");
		System.out.println();


		for(int index1=1;index1<=5;index1++) // Outer Loop
		{
		for(int index2=0;index2<=2;index2++) // Inner Loop
		{
		System.out.print(index2+" ");
		}
		System.out.println();
		}

		System.out.println(" ****************** 4 ****************");
		System.out.println();

		for(int index1=1;index1<=5;index1++) // Outer Loop
		{
		for(int index2=0;index2<=index1;index2++) // Inner Loop
		{
		System.out.print(index2+" ");
		}
		System.out.println();
		}

		System.out.println(" ****************** 5 ****************");
		System.out.println();

		for(int index1=5;index1>=0;index1--)  //outer loop
		{
		for(int index2=index1;index2>=0;index2--)  //inner loop
		{
		System.out.print(index2 +"  ");      
		}
		System.out.println();
		}

		System.out.println(" ****************** 6 ****************");
		System.out.println();


		for(int index1=5;index1>=1;index1--)   //outer loop
		{
		for(int index2=1;index2<index1;index2++)// inner loop
		{
		System.out.print(index1 +"  ");      
		}
		System.out.println();
		}

		System.out.println(" ****************** 7 ****************");
		System.out.println();

		for(int index1=5;index1>=1;index1--)   //outer loop
		{
		for(int index2=1;index2<index1;index2++)  //inner loop
		{
		System.out.print(" * ");      
		}
		System.out.println();
		}
		}
		}



