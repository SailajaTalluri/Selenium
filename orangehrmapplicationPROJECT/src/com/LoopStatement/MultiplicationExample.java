package com.LoopStatement;

public class MultiplicationExample {

	public static void main(String[] args) {
		int i=1;
		int j=0;		
		for (i=1;i<=10;i++)
		{
		for (j=0;j<1;j++)
			System.out.println("2*"+i+"="+2*i);	
		}
	}
}
	

